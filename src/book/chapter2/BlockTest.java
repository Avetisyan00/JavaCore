package book.chapter2;

public class BlockTest {
    public static void main(String[] args) {
        int x,y;
        y = 20;
        for (x = 0; x < 10; x++) {
            System.out.println("X is equal to " + x);
            System.out.println("Y is equal to " + y);
            y-=2;
        }
    }



}
